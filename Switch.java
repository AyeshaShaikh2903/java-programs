public class Switch{
public static void main(String args[])
{
int n=11;
switch(n){
case 1:
System.out.println("equal to 11");
break;
case 11:
System.out.println("equal to 11");
break;
case -3:
System.out.println("equal to -3");
break;
default:
System.out.println("no match found");
break;
}
}
}